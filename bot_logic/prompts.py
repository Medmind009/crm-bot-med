# Карта статусов для нормализации
STATUS_MAP = {
    "новый": "Новый",
    "интерес": "Интерес", 
    "переговоры": "Переговоры",
    "ожидание ответа": "Ожидание ответа",
    "предложено": "Предложено",
    "неактуально": "Неактуально",
    "отказ": "Отказ",
    "сделка": "Сделка",
}

# Карта приоритетов
PRIORITY_MAP = {
    "низкий": "Низкий",
    "средний": "Средний",
    "высокий": "Высокий",
}

def build_user_prompt(
    chat_id: str,
    username: str,
    name: str,
    report_text: str
) -> str:
    """Построить промпт пользователя из шаблона."""
    return USER_PROMPT_TEMPLATE.format(
        chat_id=chat_id,
        username=username,
        name=name,
        report_text=report_text
    )


def normalize_status(status: str) -> str:
    """Нормализовать статус лида."""
    status_lower = status.lower().strip()
    return STATUS_MAP.get(status_lower, "Новый")


def normalize_priority(priority: str) -> str:
    """Нормализовать приоритет."""
    priority_lower = priority.lower().strip()
    return PRIORITY_MAP.get(priority_lower, "Средний")